    public void AccountTable()
    {
        try{
            String sql="SELECT EMPLOYEE.Emp_ID,Emp_Account,Emp_Balance,Emp_Name,Emp_Address,Emp_City,Emp_Nationality,Emp_Email,Emp_PhoneNo FROM EMPLOYEE,EMPLOYEE_ACCOUNT WHERE EMPLOYEE.Emp_ID=EMPLOYEE_ACCOUNT.Emp_AccID";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
            System.out.println("EMPLOYEE ACCOUNT Error: "+ex);
        }
        finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
    
